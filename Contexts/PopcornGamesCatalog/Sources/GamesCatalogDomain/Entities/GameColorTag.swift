import Foundation

///
/// Defines color tags for visual categorization of games in the catalog.
///
/// Color tags provide quick visual identification and grouping of different game types
/// in the games catalog interface. Each color can be associated with a specific game
/// category or theme to help users quickly locate games of interest.
///
public enum GameColorTag: Sendable {

    /// Blue color tag for game categorization.
    case blue

    /// Green color tag for game categorization.
    case green

    /// Red color tag for game categorization.
    case red

    /// Yellow color tag for game categorization.
    case yellow

}
